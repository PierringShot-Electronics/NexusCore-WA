#!/usr/bin/env bash
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$REPO_ROOT"

LOG_DIR="${REPO_ROOT}/logs"
mkdir -p "${LOG_DIR}"
mkdir -p "${REPO_ROOT}/data/whatsapp-gateway/session"

log() {
  printf '[start_clean] %s\n' "$*" >&2
}

warn() {
  printf '[start_clean][XƏBƏRDARLIQ] %s\n' "$*" >&2
}

fatal() {
  printf '[start_clean][XƏTA] %s\n' "$*" >&2
  exit 1
}

require_cmd() {
  if ! command -v "$1" >/dev/null 2>&1; then
    fatal "'$1' tapılmadı. Zəhmət olmasa quraşdırın."
  fi
}

require_cmd docker
require_cmd curl
require_cmd python3

if ! docker compose version >/dev/null 2>&1; then
  fatal "Docker Compose v2 tələb olunur (docker compose)."
fi

if [ -f "$REPO_ROOT/.env" ]; then
  log ".env faylı tapıldı, port dəyişənləri yüklənir."
  # shellcheck disable=SC1090
  set -a
  source "$REPO_ROOT/.env"
  set +a
else
  warn ".env faylı tapılmadı, default dəyərlər istifadə olunacaq."
fi

ensure_env_default() {
  local var_name="$1"
  local default_value="$2"
  if [[ -z "${!var_name:-}" ]]; then
    eval "export ${var_name}='${default_value}'"
    warn "${var_name} təyin edilməmişdi, default ${default_value} istifadə olunur."
  else
    export "${var_name?}"
  fi
}

ensure_env_default "WHATSAPP_GATEWAY_SESSION" "default"
ensure_env_default "WHATSAPP_GATEWAY_BASE_URL" "http://wweb:3001"
ensure_env_default "WHATSAPP_GATEWAY_HOST_URL" "http://localhost:3001"
ensure_env_default "WHATSAPP_GATEWAY_WEBHOOK_URL" "http://app:3000/webhook"
ensure_env_default "WHATSAPP_GATEWAY_WEBHOOK_EVENTS" "message,session.status"
ensure_env_default "APP_PORT" "3000"
ensure_env_default "WHATSAPP_GATEWAY_PORT" "3001"
ensure_env_default "DASHBOARD_PORT" "3002"
ensure_env_default "REDIS_PORT" "6379"
ensure_env_default "MCP_PORT" "3030"
ensure_env_default "POSTGRES_PORT" "55432"
ensure_env_default "REDIS_URL" "redis://redis:${REDIS_PORT}"
ensure_env_default "DATABASE_URL" "postgres://nexuscore:nexuscore@postgres:${POSTGRES_PORT}/nexuscore"
ensure_env_default "OPENAI_MODEL" "gpt-5.1"
ensure_env_default "OPENAI_VISION_MODEL" "gpt-4o"
ensure_env_default "OPENAI_TRANSCRIPTION_MODEL" "gpt-4o-mini-transcribe"
ensure_env_default "OPENAI_TTS_MODEL" "gpt-4o-mini-tts"
ensure_env_default "OPENAI_ROUTER_MODEL" "gpt-5-nano"
ensure_env_default "AGENT_MODEL_GENERAL" "gpt-5.1"
ensure_env_default "AGENT_MODEL_SALES" "gpt-5-mini"
ensure_env_default "AGENT_MODEL_SUPPORT" "gpt-4.1-mini"
ensure_env_default "AGENT_MODEL_DIAGNOSTICS" "gpt-4o"

